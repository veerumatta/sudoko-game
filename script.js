let currentBoard = [];

function goToDifficulty() {
    document.getElementById('welcome-screen').style.display = 'none';
    document.getElementById('difficulty-screen').style.display = 'flex';
    document.getElementById('game-screen').style.display = 'none';
}

function showGame(level) {
    currentDifficulty = level;
    document.getElementById('difficulty-screen').style.display = 'none';
    document.getElementById('game-screen').style.display = 'flex';
    startNewGame();
}

function showMenu() {
    document.getElementById('welcome-screen').style.display = 'flex';
    document.getElementById('difficulty-screen').style.display = 'none';
    document.getElementById('game-screen').style.display = 'none';
    if (timerInterval) clearInterval(timerInterval);
}

function startNewGame() {
    const list = puzzles[currentDifficulty];
    const randomIndex = Math.floor(Math.random() * list.length);
    currentBoard = list[randomIndex].map(row => [...row]);
    seconds = 0;
    if (timerInterval) clearInterval(timerInterval);
    startTimer();
    document.getElementById('diff-label').innerText = currentDifficulty.toUpperCase();
    initBoard();
}

function initBoard() {
    gridElement.innerHTML = "";
    for (let r = 0; r < 9; r++) {
        for (let c = 0; c < 9; c++) {
            const cell = document.createElement('div');
            cell.classList.add('cell');
            if (currentBoard[r][c] !== 0) {
                cell.innerText = currentBoard[r][c];
                cell.classList.add('fixed');
            } else {
                cell.addEventListener('click', () => {
                    if (selectedCell) selectedCell.classList.remove('selected');
                    selectedCell = cell;
                    selectedCell.classList.add('selected');
                });
            }
            cell.dataset.row = r; cell.dataset.col = c;
            gridElement.appendChild(cell);
        }
    }
}

function inputNumber(num) {
    if (selectedCell && !selectedCell.classList.contains('fixed')) {
        const r = selectedCell.dataset.row, c = selectedCell.dataset.col;
        currentBoard[r][c] = num;
        selectedCell.innerText = num || "";
        selectedCell.classList.remove('error');
        void selectedCell.offsetWidth;
        if (num !== 0 && isInvalid(r, c, num)) selectedCell.classList.add('error');
    }
}

function isInvalid(r, c, num) {
    r = parseInt(r); c = parseInt(c);
    for (let i = 0; i < 9; i++) {
        if (i !== c && currentBoard[r][i] === num) return true;
        if (i !== r && currentBoard[i][c] === num) return true;
    }
    const sr = Math.floor(r / 3) * 3, sc = Math.floor(c / 3) * 3;
    for (let i = 0; i < 3; i++) {
        for (let j = 0; j < 3; j++) {
            if ((sr+i !== r || sc+j !== c) && currentBoard[sr+i][sc+j] === num) return true;
        }
    }
    return false;
}

function startTimer() {
    timerInterval = setInterval(() => {
        seconds++;
        const m = Math.floor(seconds / 60).toString().padStart(2, '0');
        const s = (seconds % 60).toString().padStart(2, '0');
        document.getElementById('timer').innerText = `${m}:${s}`;
    }, 1000);
}

function checkWin() {
    const hasErrors = document.querySelectorAll('.error').length > 0;
    const isFull = Array.from(document.querySelectorAll('.cell')).every(c => c.innerText !== "");
    if (isFull && !hasErrors) {
        confetti({ particleCount: 150, spread: 70, origin: { y: 0.6 } });
        alert("Sudoku Mastered!");
    } else { alert("Not finished or contains mistakes!"); }
}

document.addEventListener('keydown', (e) => {
    if (e.key >= '1' && e.key <= '9') inputNumber(parseInt(e.key));
    if (e.key === 'Backspace' || e.key === 'Delete') inputNumber(0);
});